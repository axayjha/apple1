/**
 * roms_builtin.h - Built-in ROM images for the Apple 1 emulator
 *
 * Contains the Woz Monitor ROM (256 bytes, loaded at $FF00) and a
 * placeholder BASIC stub for the $E000 region.
 *
 * The Woz Monitor was written by Steve Wozniak and published in the
 * Apple 1 Operation Manual (1976). It provides a simple hex monitor
 * with memory examine/deposit capabilities.
 *
 * Key entry points:
 *   $FF00  RESET  - Entry point (CLD, CLI, initialize PIA)
 *   $FFDC  PRBYTE - Print accumulator as two hex digits
 *   $FFE5  PRHEX  - Print low nibble of accumulator as hex
 *   $FFEF  ECHO   - Output character in accumulator to display
 *
 * The ROM occupies $FF00-$FFFF with 6502 vectors at the end:
 *   $FFFA-$FFFB  NMI vector   -> $0F00
 *   $FFFC-$FFFD  RESET vector -> $FF00
 *   $FFFE-$FFFF  IRQ vector   -> $0000
 */

