/**
 * BASIC stub - placeholder for Integer BASIC ($E000 region)
 *
 * Since the original Apple 1 Integer BASIC is copyrighted, this stub
 * simply jumps back to the Woz Monitor. When BASIC is invoked (by
 * typing E000R in the monitor), the user will be returned to the
 * monitor prompt.
 *
 * A real BASIC ROM can be loaded from file to replace this stub.
 *
 * Entry points:
 *   $E000  Cold start (JMP to monitor GETLINE at $FF1F)
 *   $E2B3  Warm start (JMP to monitor GETLINE at $FF1F)
 */
static const uint8_t basic_stub[2048] = {
    /* $E000: Cold start - jump to monitor */
    0x4C, 0x1F, 0xFF,  /* JMP $FF1F (GETLINE in Woz Monitor) */
    /* $E003-$E2B2: padding to reach warm start entry */
    [0x02B3] = 0x4C,   /* $E2B3: JMP $FF1F */
    [0x02B4] = 0x1F,
    [0x02B5] = 0xFF,
    /* Remaining bytes are zero-initialized by default */
};

/**
 * Load the built-in Woz Monitor ROM into emulator memory at $FF00.
 * Optionally loads the BASIC stub at $E000.
 */
void roms_load_builtin(void);

